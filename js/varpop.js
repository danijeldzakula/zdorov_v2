function showVisitors() {
    //	Функция выборки случайного числа
    function getRandom(min, max) {
        var res = Math.floor(Math.random() * (max - min + 1) + min);
        return res;
    }

    //  Количество посетителей
    var countPeoples = getRandom(130, 230);
    var peoplesBlock = $('#peoples');
    var min = countPeoples - 20;
    var max = countPeoples + 20;
    peoplesBlock.text(getRandom(min, max));

    //  Показ кол-ва посетителей на сайте
    setInterval(function () {
        peoplesBlock.fadeOut(function () {
            peoplesBlock.text(getRandom(min, max));
            peoplesBlock.fadeIn();
        });
    }, 8000);
}

function changeQuantity() {
    //Правильные окончания слов
    function declOfNum(number, titles) {
        cases = [2, 0, 1, 1, 1, 2];
        return titles[(number % 100 > 4 && number % 100 < 20) ? 2 : cases[(number % 10 < 5) ? number % 10 : 5]];
    }

    //Кол-во заказанных упаковок
    var purCnt = Math.floor(Math.random() * (6 - 3 + 1) + 3);
    var purCntString = purCnt + ' ' + declOfNum(purCnt, ['упаковка', 'упаковки', 'упаковок']);
    if (~siteName.indexOf("de.")) {//немцы
        var purCntString = purCnt + ' ' + declOfNum(purCnt, ['Verpackungen', 'Verpackungen', 'Verpackungen']);
    }

    $('#purCnt').text(purCnt);
    $('#purCntStr').text(purCntString);
}

function randSex() {

    var isShow = Math.round(Math.random());
    if (~siteName.indexOf("vari")
        || ~siteName.indexOf("wrinkles")
        || ~siteName.indexOf("breast")
        || ~siteName.indexOf("cellulitis")) {
        sex = 'w'
    } else if (~siteName.indexOf("prostatit")) {
        sex = 'm';
    } else if (parseInt(isShow) == 1) {
        sex = 'w'
    } else {
        var sex = 'm';
    }
    ;
    return sex;
}

function randAtZeroToNine() {
    var rand = -0.5 + Math.random() * 10;
    rand = Math.round(rand);
    return rand;
}

